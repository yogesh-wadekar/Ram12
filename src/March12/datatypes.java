package March12;

public class datatypes {
	public static void main(String[] args)
	{
	boolean a=true;
	System.out.print("Boolean= ");
	System.out.print(a);
	System.out.println("				//only store True/False ");
	boolean b=false;
	System.out.print("Boolean= ");
	System.out.print(b);
	System.out.println("				//only store True/False ");
	int c=1234567890;
	System.out.print("Integer datatype= ");
	System.out.print(c);
	System.out.println("		//It print only 10 digits ");
	byte d=127;
	System.out.print("Byte datatype= ");
	System.out.print(d);
	System.out.println("			//It print only 3 digits ");
	short e=32767;
	System.out.print("Short datatype= ");
	System.out.print(e);
	System.out.println("			//It print only 5 digits ");
	long f=1234567890;
	System.out.print("Long datatype= ");
	System.out.print(f);
	System.out.println("		//It also print only 10 digits like int. ");
	double g=123.1234567890;
	System.out.print("Double datatype= ");
	System.out.print(g);
	System.out.println("		//It print N numbers after dot");
	float h=123.123456789f;
	System.out.print("Float datatype= ");
	System.out.print(h);
	System.out.println("		//It print only 5 digit after dot(.) and round 5th digit");
	String i="String datatype= Hello Java JDK@17";
	System.out.print(i);
	System.out.println("	//It print N numbers of set of char., number and special sysmbol");
}
}